void drawTitle ( void )
{
	//変数宣言
	int x, y;		//タイトルのX、Y座標
	char title[20][100] =
	{
		"                   ##########     ###########  #########                       ",
		"                   ##       ##    ##           ##       ##                     ",
		"                   ##        ##   ##           ##        ##                    ",
		"                   ##       ##    ##           ##         ##                   ",
		"                   ##########     #########    ##         ##                   ",
		"                   ##     ##      ##           ##         ##                   ",
		"                   ##      ##     ##           ##        ##                    ",
		"                   ##       ##    ##           ##       ##                     ",
		"                   ##        ##   ###########  #########                       ",
		"                                                                               ",
		"                                                                               ",
		" ##########       #####        ######    ##     ##    #########  ##############",
		" ##       ##    ##     ##    ##     ##   ##    ##     ##               ##      ",
		" ##        ##  ##       ##  ##       ##  ##   ##      ##               ##      ",
		" ##       ##   ##       ##  ##       ##  ##  ##       ##               ##      ",
		" ##########    ##       ##  ##           ## ##        #######          ##      ",
		" ##     ##     ##       ##  ##       ##  #####        ##               ##      ",
		" ##      ##    ##       ##  ##       ##  ##   ##      ##               ##      ",
		" ##       ##    ##     ##    ##     ##   ##     ##    ##               ##      ",
		" ##        ##     #####        #####     ##       ##  #########        ##      ",
	};

	//-------------------------------
	//タイトルロゴ描写
	//-------------------------------
	CLS(WHITE, RED);
	COLOR(WHITE, RED);
	for( y = 0; y < 20; y++ )
	{
		LOCATE(1, 2 + y);
		for( x = 0; x < 80; x++ )
		{
			printf("%c",title[y][x]);
		}
	}

	COLOR(WHITE, RED);
	LOCATE(30, 24);
	printf("[]    START ENTER    []");
}

